/*
 * Q.
 * JDKに含まれるsrc.zipファイルを展開しなさい。
 * Files.walkを使用して、予約語であるtransientとvolatileを含むJavaのファイルをすべて見つけなさい。
 */

package jp.co.namihira.java8;

import java.io.IOException;
import java.io.UncheckedIOException;
import java.nio.charset.StandardCharsets;
import java.nio.file.Files;
import java.nio.file.Path;
import java.nio.file.Paths;
import java.util.stream.Stream;

public class Main {

    public static void main(String[] args) {
        final Path fileFullPath = Paths.get(getJdkHome(), "src.zip");
        final Path distPath = Paths.get(".", "dist");

        FileUtils.unzip(fileFullPath, distPath);

        try (Stream<Path> entries = Files.walk(distPath)) {
            entries.filter(e -> !Files.isDirectory(e))
                   .filter(e -> {
                        byte[] bytes;
                        try {
                            bytes = Files.readAllBytes(e);
                            final String content = new String(bytes, StandardCharsets.UTF_8);
                            return content.contains("transient") || content.contains("volatile");
                        } catch (IOException e1) {
                            throw new UncheckedIOException(e1);
                        }
                    })
                   .sorted()
                   .forEach(System.out::println);
        } catch (IOException e) {
            throw new UncheckedIOException(e);
        }
    }

    public static String getJdkHome() {
        final String jreHome = System.getProperty("java.home");
        return jreHome.substring(0, jreHome.lastIndexOf("\\"));
    }

}

